import 'dart:async';

import 'package:flutter/material.dart';

import '/_common.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

void showLoadingOverlayUntilComplete(
  BuildContext context,
  Future<void> future,
) {
  showMessageOverlay(
    context,
    leading: CircularProgressIndicator.adaptive(strokeWidth: 4.sc),
    remover: (remove) async {
      await future;
      remove();
    },
  );
}
