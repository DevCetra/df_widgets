void showLoadingOverlayUntilComplete(
  BuildContext context,
  Future<void> future,
  void Function(Object? e) onError,
) {
  VoidCallback? remove;
  future.onError((e, stackTrace) {
    if (context.mounted) {
      remove?.call();
      onError.call(e);
    }
  });
  showMessageOverlay(
    context,
    leading: const BasicDelayedCircularProgressIndicator(
      delay: Duration.zero,
    ),
    remover: (r) async {
      remove = r;
      await future;
      r();
    },
  );
}
