class BasicBackBtn extends StatelessWidget {
  final Pod<bool> pState;
  final _OnTap? onTap;
  final _OnTapDown? onTapDown;

  const BasicBackBtn({
    super.key,
    required this.pState,
    this.onTap,
    this.onTapDown,
  });

  @override
  Widget build(BuildContext context) {
    return BasicIconBtn(
      onTap: onTap,
      onTapDown: onTapDown,
      iconData: FluentIcons.arrow_left_24_regular,
    );
  }
}

